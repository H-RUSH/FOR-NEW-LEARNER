#include <stdio.h>
#include <windows.h>  // Sleep ফাংশনের জন্য

int main() {
    // প্রথম লাইন প্রিন্ট করো
    printf("প্রথম লাইন\n");

    // ১ সেকেন্ড অপেক্ষা করো
    Sleep(1000);  // মিলিসেকেন্ডে সময় দেয়া হয়

    // দ্বিতীয় লাইন প্রিন্ট করো
    printf("দ্বিতীয় লাইন\n");

    return 0;
}
